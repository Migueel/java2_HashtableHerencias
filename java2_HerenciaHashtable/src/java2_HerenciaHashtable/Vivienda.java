package java2_HerenciaHashtable;

public abstract class Vivienda {
	int tamano;
	int num;

	public Vivienda(){
		
	}

	public int getTamano() {
		return tamano;
	}

	public void setTamano(int tamano) {
		this.tamano = tamano;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
	
}


